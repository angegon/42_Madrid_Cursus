#include "libft.h"

int		ft_atoi(char *str)
{
	int i;
	int contneg;
	int result;

	i = 0;
	contneg = 1;
	result = 0;
	while ((str[i] != '\0') && (str[i] == '\t' || str[i] == '\n' ||
	str[i] == '\v' || str[i] == '\f' || str[i] == '\r' || str[i] == ' '))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			contneg = -1;
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (contneg * result);
}
